function TrademarkEnableDisableTextBox(trademarkOthers) {
    var tradecheckother = document.getElementById("tradecheckother");
    tradecheckother.disabled = trademarkOthers.checked ? false : true;
    if (!tradecheckother.disabled) {
        tradecheckother.focus();
    }
}

